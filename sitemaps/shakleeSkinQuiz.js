function normalize(str) {
  return String(str || '')
    .toLowerCase()
    .replace(/\\"/g, '"')
    .replace(/["']/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

function findDemographicKey(gender, ageRange) {
  const gKey = normalize(gender);
  const aKey = normalize(ageRange);
  return Object.keys(productMatrix).find(fullKey =>
    normalize(fullKey).includes(gKey) &&
    normalize(fullKey).includes(aKey)
  );
}

function findConcernKey(bucket, concern) {
  const want = normalize(concern);
  for (let k of Object.keys(bucket)) {
    if (normalize(k) === want) return k;
  }
  for (let k of Object.keys(bucket)) {
    const n = normalize(k);
    if (n.includes(want) || want.includes(n)) return k;
  }
  return null;
}

